namespace Stringier.Streams {
	/// <summary>
	/// The encoding of a <see cref="TextStream"/>.
	/// </summary>
	public enum Encoding {
		/// <summary>
		/// UTF-8
		/// </summary>
		UTF8,

		/// <summary>
		/// UTF-16 big endian
		/// </summary>
		UTF16BE,

		/// <summary>
		/// UTF-16 little endian
		/// </summary>
		UTF16LE,

		/// <summary>
		/// UTF-32 big endian
		/// </summary>
		UTF32BE,

		/// <summary>
		/// UTF-32 little endian
		/// </summary>
		UTF32LE,

	}
}
